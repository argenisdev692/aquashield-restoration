import * as React from 'react';

interface CompanyDataStatusBadgeProps {
  // If your domain adds a status field to CompanyData in the future,
  // change this prop. For now, it's a simple placeholder or 'active'
  status?: 'active' | 'suspended' | 'banned' | 'deleted';
}

const STATUS_CONFIG: Record<
  string,
  { label: string; bg: string; text: string; dot: string }
> = {
  active: {
    label: 'Verified',
    bg: 'color-mix(in srgb, var(--accent-success) 15%, transparent)',
    text: 'var(--accent-success)',
    dot: 'var(--accent-success)',
  },
  suspended: {
    label: 'Suspended',
    bg: 'color-mix(in srgb, var(--accent-warning) 15%, transparent)',
    text: 'var(--accent-warning)',
    dot: 'var(--accent-warning)',
  },
  deleted: {
    label: 'Deleted',
    bg: 'color-mix(in srgb, var(--text-disabled) 15%, transparent)',
    text: 'var(--text-disabled)',
    dot: 'var(--text-disabled)',
  },
};

export default function CompanyDataStatusBadge({
  status = 'active',
}: CompanyDataStatusBadgeProps): React.JSX.Element {
  const config = STATUS_CONFIG[status] ?? STATUS_CONFIG.active;

  return (
    <span
      className="inline-flex items-center gap-1.5 rounded-full px-2.5 py-0.5 text-[11px] font-semibold"
      style={{
        background: config.bg,
        color: config.text,
        fontFamily: 'var(--font-sans)',
      }}
    >
      <span
        className="h-1.5 w-1.5 rounded-full"
        style={{ background: config.dot }}
      />
      {config.label}
    </span>
  );
}
